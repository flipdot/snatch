export default function generateRandomName(): string {
	// words.length is about 1600
	// We use three words to accomplish possible combinations, which is about Math.log2(1600 ** 3) = 31 ~bits of information
	// Basically, we can have about an int32 amount of file names

	// We use secure random to avoid file names being guessable by predicting the RNG output
	const [first, second, third] = [
		(Math.random() * words.length) | 0,
		(Math.random() * words.length) | 0,
		(Math.random() * words.length) | 0,
	];

	return `${words[first]}-${words[second]}-${words[third]}`;
}
